    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultTableModel tableModel = (DefaultTableModel) tbBook.getModel();
        for (AddKitabxana ak : db.allBook()) {
            tableModel.addRow(new Object[]{ak.getId(), ak.getBookName(), ak.getYaziciName(), ak.getBookSort(), ak.getRefNumber(), ak.getSiraNumber(), ak.getSutunNumber(), ak.getBookAbout()});
        }
    }//GEN-LAST:event_formWindowOpened

