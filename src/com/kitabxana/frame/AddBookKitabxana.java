    private void tbBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbBookMouseClicked
        int rowNum = tbBook.getSelectedRow();
        tfBookId.setText(tbBook.getValueAt(rowNum, 0).toString());
        tfBookName.setText(tbBook.getValueAt(rowNum, 1).toString());
        tfYaziciName.setText(tbBook.getValueAt(rowNum, 2).toString());

        tfRefNumber.setText(tbBook.getValueAt(rowNum, 5).toString());
        tfSiraNumber.setText(tbBook.getValueAt(rowNum, 6).toString());
        tfSutunNumber.setText(tbBook.getValueAt(rowNum, 7).toString());
        taBookAbout.setText(tbBook.getValueAt(rowNum, 8).toString());
    }//GEN-LAST:event_tbBookMouseClicked

    private void btnUpdateBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateBookActionPerformed
        AddKitabxana ak = new AddKitabxana();

        ak.setId(Integer.parseInt(tfBookId.getText()));
        ak.setBookName(tfBookName.getText());
        ak.setYaziciName(tfYaziciName.getText());

        ak.setBookSort(cmbBookSort.getSelectedItem().toString());
        ak.setRefNumber(tfRefNumber.getText());
        ak.setSiraNumber(tfSiraNumber.getText());
        ak.setSutunNumber(tfSutunNumber.getText());
        ak.setBookAbout(taBookAbout.getText());
        db.updateBooksByID(ak);

        DefaultTableModel tableModel = (DefaultTableModel) tbBook.getModel();
        tableModel.setRowCount(0);
        formWindowOpened(null);

        tfBookName.setText("");
        tfYaziciName.setText("");

        tfRefNumber.setText("");
        tfSiraNumber.setText("");
        tfSutunNumber.setText("");
        tfBookId.setText("");
        taBookAbout.setText("");
    }//GEN-LAST:event_btnUpdateBookActionPerformed

