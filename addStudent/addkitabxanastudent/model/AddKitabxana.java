
package com.addkitabxanastudent.model;

public class AddKitabxana {
    
    private int id;
    private String bookName;
    private String yaziciName;
    
    private String bookSort;
    private int refNumber;
    private int siraNumber;
    private int sutunNumber;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBookName() {
        return bookName;
    }

    public void setBookName(String bookName) {
        this.bookName = bookName;
    }

    public String getYaziciName() {
        return yaziciName;
    }

    public void setYaziciName(String yaziciName) {
        this.yaziciName = yaziciName;
    }

    
    public String getBookSort() {
        return bookSort;
    }

    public void setBookSort(String bookSort) {
        this.bookSort = bookSort;
    }

    public int getRefNumber() {
        return refNumber;
    }

    public void setRefNumber(int refNumber) {
        this.refNumber = refNumber;
    }

    public int getSiraNumber() {
        return siraNumber;
    }

    public void setSiraNumber(int siraNumber) {
        this.siraNumber = siraNumber;
    }

    public int getSutunNumber() {
        return sutunNumber;
    }

    public void setSutunNumber(int sutunNumber) {
        this.sutunNumber = sutunNumber;
    }
    
    
    
}
