
package com.addkitabxanastudent.model;

public class UnivercityStudent {
    
    private int id;
    private String name;
    private String surname;
    private String fatherName;
    private String age;
    private String tbSeriaNumber;
    private String epoct;
    private String mobilPhone;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getFatherName() {
        return fatherName;
    }

    public void setFatherName(String fatherName) {
        this.fatherName = fatherName;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }


    public String getTbSeriaNumber() {
        return tbSeriaNumber;
    }

    public void setTbSeriaNumber(String tbSeriaNumber) {
        this.tbSeriaNumber = tbSeriaNumber;
    }

    public String getEpoct() {
        return epoct;
    }

    public void setEpoct(String epoct) {
        this.epoct = epoct;
    }

    public String getMobilPhone() {
        return mobilPhone;
    }

    public void setMobilPhone(String mobilPhone) {
        this.mobilPhone = mobilPhone;
    }
    
    
}
